    public function getQuestionSixReponses(): array{
        /**
        * SELECT question.*, COUNT(reponse.id)  AS nb_reponses FROM question
        *  INNER JOIN reponse ON reponse.question_id = question.id
        *  GROUP BY question.id
        *  HAVING COUNT(reponse.id) > 6
        */

        return $this->createQueryBuilder('question')
            ->innerJoin('question.reponses', 'reponse')
            ->groupBy('question.id')
            ->having('COUNT(reponse.id) > 6')
            ->getQuery()
            ->getResult()
        ;
    }

